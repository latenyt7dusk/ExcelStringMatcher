    private void jButton1ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButton1ActionPerformed
        // TODO add your handling code here:
        try{
            int Con = FileChooser.showOpenDialog(this);
            if(Con == JFileChooser.APPROVE_OPTION){
                if(FileChooser.getSelectedFile() != null){
                    jTextField1.setText(FileChooser.getSelectedFile().getAbsolutePath());
                    Primary = new MatchSource(FileChooser.getSelectedFile().getAbsolutePath());
                    Primary_Sheets.removeAllItems();
                    for(String x:Primary.getSheets()){
                        Primary_Sheets.addItem(x);
                    }
                }
            }
        }catch(Exception er){
            
        }
    }//GEN-LAST:event_jButton1ActionPerformed

