    private void ProgressButtonActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_ProgressButtonActionPerformed
        // TODO add your handling code here:
        if(Primary != null || Secondary != null){
           Primary.setStartIndex(Integer.parseInt(Primary_Start.getValue().toString()));
           Primary.setEndIndex(Integer.parseInt(Primary_End.getValue().toString()));
           Primary.setSourceColumn(Primary_Column.getText().toUpperCase());
           Primary.CheckDuplicates(jCheckBox1.isSelected());
           
           Secondary.setStartIndex(Integer.parseInt(Secondary_Start.getValue().toString()));
           Secondary.setEndIndex(Integer.parseInt(Secondary_End.getValue().toString()));
           Secondary.setSourceColumn(Secondary_Column.getText().toUpperCase());
           Secondary.CheckDuplicates(jCheckBox2.isSelected());
           
           Primary.setTargetColumn(Target_Column.getText().toUpperCase());
           
           MatchProg = new MatchProgress(Primary,Secondary,Integer.parseInt(Sensitivity_Ratio.getValue().toString()));
           ProcThread = new Thread(MatchProg);
           ProcThread.start();
        }
    }//GEN-LAST:event_ProgressButtonActionPerformed

